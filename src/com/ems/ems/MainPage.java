    private void registerStudent(String email, long cn) {
        
        String fname = studentFirstNameTextField.getText().trim();
        String lname = studentLastNameTextField.getText().trim();
        
        Date currentDate = new Date();
        long cl = currentDate.getTime();
        
        
        Date d = dobDateChooser.getDate();
        long dob;
        try {
            dob = d.getTime();
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Choose date of birth", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        long diff = cl - dob;
        
        if (diff <= 0) {
            JOptionPane.showMessageDialog(null, "Are you kidding ? \n Select right date of birth.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String deg = qualificationComboBox.getSelectedItem().toString();
        String d2;
        if (deg.equals("Other")) {
            d2 = otherDegreeTextField.getText().trim();
            if (d2.equals("") || d2.equals("In case of other specify here")) {
                JOptionPane.showMessageDialog(null, "Please, specify degree", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            deg = d2;
        }
        
        int marks = Integer.parseInt(marksSpinner.getValue().toString());
        
        if (fname.equals("") || lname.equals("") || deg.equals("") || marks < 0 || marks > 100) {
            JOptionPane.showMessageDialog(null, "All fields are necessary and"
                    + " marks must be in the range [0, 100]", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Student student = new Student(fname, lname, dob, deg, marks, cn, email);
        int resultCode = student.getNumberOfStudents();
        if (resultCode == -1) {
            JOptionPane.showMessageDialog(null, "This email address is already registered", "Error", JOptionPane.ERROR_MESSAGE);
            registerEmailTextField.setText("");
            return;
        }
        
        studentFirstNameTextField.setText("");
        studentLastNameTextField.setText("");
        marksSpinner.setValue(0);
        registerEmailTextField.setText("");
        registerContactTextField.setText("");
        dobDateChooser.cleanup();
        
        student.setId(resultCode + 1);
        int result = student.RegisterStudent("1");
        switch (result) {
            case 0:
                JOptionPane.showMessageDialog(null, "Can not register at the moment, try later", "Error", JOptionPane.ERROR_MESSAGE);
                break;
        }
        
    }
    private void registerCompany(String email, long cn) {
        String name = companyNameTextField.getText();
        String address = companyAddressTextField.getText();
        String profileLink = companyProfileLinkTextField.getText();
        
        if (name.equals("") || address.equals("") || profileLink.equals("") || 
                profileLink.equals("Url of your company profile")) {
            JOptionPane.showMessageDialog(this, "All fields are necessary ", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Company company = new Company(name, address, profileLink, email, cn);
        int resultCode = company.getNumberOfCompanies();
        if (resultCode == -1) {
            JOptionPane.showMessageDialog(this, "This email address is already registered","Error", JOptionPane.ERROR_MESSAGE);
            registerEmailTextField.setText("");
            return;
        }
        
        companyNameTextField.setText("");
        companyAddressTextField.setText("");
        companyProfileLinkTextField.setText("");
        registerEmailTextField.setText("");
        registerContactTextField.setText("");
        
        company.setId(resultCode + 1);
        company.RegisterCompany("1");
    }
