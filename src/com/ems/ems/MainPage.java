    private void loginStudent(String email, String pass) {
        
        Student student = null;
        boolean isLoggedIn = false;
        String isFirstTime = "0";
        
        try {
            CsvReader students = new CsvReader("students.csv");
            
            students.readHeaders();
            
            while (students.readRecord()) {
                String e = students.get("username");
                String p = students.get("password");
                
                if (email.equalsIgnoreCase(e) && pass.equals(p)) {
                    // registration success
                    isLoggedIn = true;
                    student = new Student(
                            students.get("firstName")
                            , students.get("lastName")
                            , Long.parseLong(students.get("dob"))
                            , students.get("degree")
                            , Integer.parseInt(students.get("marks"))
                            , Long.parseLong(students.get("contactNumber"))
                            , students.get("emailId")
                    );
                    student.setId(Integer.parseInt(students.get("id")));
                    isFirstTime = students.get("isFirstTime");
                    break;
                }
            
            }
            
            students.close();
            if (isLoggedIn) {
                if (isFirstTime.equals("1")) {
                    SetPasswordPage setPasswordPage = new SetPasswordPage(student, email);
                    setPasswordPage.setVisible(true);
                    dispose();
                } else {
                    StudentHome studentHome = new StudentHome(student, email);
                    studentHome.setVisible(true);
                    dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Incorrect username or/and password", "Error", JOptionPane.ERROR_MESSAGE);
                
            }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Unable to access database", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Error logging in", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

