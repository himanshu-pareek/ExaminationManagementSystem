    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String loginEmail, loginPassword;
        loginEmail = loginEmailTextField.getText().trim();
        loginPassword = loginPasswordTextField.getText().trim();
        if (loginEmail.equals("") || loginPassword.equals("")) {
            JOptionPane.showMessageDialog(null, "Both fields are necessary", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        loginEmailTextField.setText("");
        loginPasswordTextField.setText("");
        switch (userComboBox.getSelectedItem().toString()) {
            case "Student":
                loginStudent(loginEmail, loginPassword);
                break;
            case "Company":
                loginCompany(loginEmail, loginPassword);
                break;
            default :
                JOptionPane.showMessageDialog(null, "error login", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

